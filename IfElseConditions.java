package com.ujjawal.conditionals;

public class IfElseConditions {

	public static void main(String[] args) {
		int i = 40;
		if (i > 40) {

			System.out.println("1");
		}
		else if (i > 35) {
			System.out.println("3");
		}

		else if (i < 50) {
			System.out.println("2");
		}

		else {
			System.out.println("I is neither 25 nor 24");
		}

	}

}
